function load_select_options( $field ) {

    if(strpos($field['name'], 'background_color') !== false) {
        $field['choices'] = array(
            'none' => 'None',
            'text-white' => 'None, White text',
            'text-dark' => 'None, dark text',
            'red text-white' => 'Red',
            'aqua text-white' => 'Aqua',
            'dark-blue text-white' => 'Blue',
            'light-gray text-dark' => 'Light gray',
            'white' => 'White',
            'lila text-white' => 'Lila',
            'gradient' => 'Gradient',
        );
    } else if(strpos($field['name'], 'gradient_color') !== false) {
        $field['choices'] = array(
            '#F7403A' => 'Red',
            '#49C3B1' => 'Aqua',
            '#C2BEC7' => 'Lila',
            '#302C45' => 'Purple',
            '#1F1A33' => 'Blue',
            '#9D9D9C' => 'Gray',
        );
    } else if(strpos($field['name'], 'text_size') !== false) {
        $field['choices'] = array(
            'normal' => 'Normal',
            'big' => 'Big',
            'extra-big' => 'Extra big',
            'super-big' => 'Super big',
            'huge text-italic' => 'Huge',
        );
    } else if(strpos($field['name'], 'post_type') !== false) {
        $field['choices'] = array(
            'post' => 'Posts',
            'ventures' => 'Ventures',
            'seeds' => 'Seeds',
            'promos' => 'Promos',
        );
    } else if(strpos($field['name'], 'column_width') !== false) {
        $field['choices'] = array(
            'col-sm-6 col-sm-offset-3 text-center' => '1/2, centered',
            'col-sm-6 text-left' => '1/2, left',
            'col-sm-8 col-sm-offset-2 text-center' => '2/3, centered',
            'col-sm-8 text-left' => '2/3, left',
            'col-sm-12 text-center' => 'full width, center',
            'col-sm-12 text-left' => 'full width, left',
        );
    }

    return $field;

}
